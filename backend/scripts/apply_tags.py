def apply_tags(path: Path, tags: dict) -> int:
    data = json.loads(path.read_text(encoding="utf-8"))
    count = 0
    for q in data["questions"]:
        qid = q["question_id"]
        if qid in tags:
            topic, subtopic = tags[qid]
            q["content"]["topic"] = topic
            q["content"]["subtopic"] = subtopic
            count += 1
        else:
            print(f"  WARNING: no tag for {qid}")
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False), encoding="utf-8")
    return count


def main() -> None:
    files = [
        "questions_2016.json",
        "questions_2017.json",
        "questions_2018.json",
        "questions_2019.json",
        "questions_2021.json",
    ]
    total = 0
    for filename in files:
        path = DATA_DIR / filename
        n = apply_tags(path, TAGS)
        print(f"{filename}: tagged {n} questions")
        total += n
    print(f"\nTotal: {total} questions tagged")


if __name__ == "__main__":
    main()
