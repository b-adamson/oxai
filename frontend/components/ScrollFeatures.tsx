// Group into pairs
const PAIRS = Array.from({ length: Math.ceil(FEATURES.length / 2) }, (_, i) =>
  FEATURES.slice(i * 2, i * 2 + 2)
);

function FeatureCard({ feature, side, state }: { feature: Feature; side: 'left' | 'right'; state: ScrollState }) {
  const offset = side === 'left' ? '-translate-x-16' : 'translate-x-16';
  const classes = [
    'transition-all duration-700 ease-out will-change-transform flex-1',
    state === 'visible' ? 'opacity-100 translate-x-0' : `opacity-0 ${offset}`,
  ].join(' ');

  return (
    <div className={classes}>
      <div className="w-20 h-20 rounded-2xl bg-accent-pale dark:bg-gray-800 flex items-center justify-center text-4xl shadow-sm mb-6">
        {feature.icon}
      </div>
      {feature.tag && (
        <div className="text-xs font-bold uppercase tracking-widest text-accent mb-2">
          {feature.tag}
        </div>
      )}
      <h3 className="text-2xl md:text-3xl font-black text-gray-900 dark:text-white leading-tight mb-4">
        {feature.title}
      </h3>
      <p className="text-base text-gray-500 dark:text-gray-400 leading-relaxed">
        {feature.body}
      </p>
    </div>
  );
}

function FeaturePair({ features }: { features: Feature[] }) {
  const { ref, state } = useScrollReveal(0.15);
  const [left, right] = features;

  return (
    <div className="min-h-[80vh] flex items-center py-20 px-6 md:px-16">
      <div ref={ref} className="max-w-5xl mx-auto w-full flex gap-12 md:gap-20 flex-col md:flex-row">
        <FeatureCard feature={left} side="left" state={state} />
        {right && <FeatureCard feature={right} side="right" state={state} />}
      </div>
    </div>
  );
}

